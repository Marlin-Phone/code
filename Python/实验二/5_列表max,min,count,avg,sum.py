# 求列表s = [9,7,8,3,2,1,55,6]中的元素个数,最大值,最小值,元素之和,平均值
s = [9,7,8,3,2,1,55,6]
count_num = len(s)
min_num = min(s)
max_num = max(s)
sum = sum(s)
avg_num = sum / count_num
print("元素个数:", count_num)
print("最大值:", max_num) 
print("最小值:", min_num)
print("元素之和", sum)
print("平均值", avg_num)
